// Package hyphenator is a word hyphenator.
//
// It provides a golang implementation of the word hyphenation algorithm
// proposed by Frank M. Liang, which is distributed
// with Tex.
//
// The hyphenator uses the same language-specific rules as those distributed with TeX.
//
// Rule files are available at https://www.tug.org/tex-hyphen.
//
// Authors may contribute to the rule sets there: https://github.com/hyphenation/tex-hyphen.
//
// Reference:
//
//	https://www.tug.org/docs/liang/liang-thesis.pdf
//
// Credits to Norbert Pillmayer, whose experimental work largely inspired this package
// (https://github.com/npillmayer/gotype),
